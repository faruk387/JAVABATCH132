public class Q04_Geometry {
    //Create a Java programme to calculate the volume of cylinder and rectangular prism.(Use inheritance)
  public double volumeOfCylinder(double radius,double height){
      return 3.14*radius*height;
  }
    public double volumeOfRectangularPrism(double width,double radius,double height){

      return width*radius*height;
    }
    public double areaOfASquare(double a){

        return a*4;

    }

}
